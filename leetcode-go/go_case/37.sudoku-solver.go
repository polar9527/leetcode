// @lc code=start
func solveSudoku(board [][]byte) {
	l := len(board)

	var backtracing func(int, int) bool
	backtracing = func(row, col int) bool {

		for r := 0; r < l; r++ {
			for c := 0; c < l; c++ {
				if board[r][c] == '.' {
					for i := '1'; i <= '9'; i++ {
						if isValid(board, r, c, byte(i)) {
							board[r][c] = byte(i)
							if backtracing(r, c) {
								return true
							}
							board[r][c] = '.'
						}
					}
					return false
				}

			}

		}
		return true
	}
	backtracing(0, 0)
}

func isValid(board [][]byte, row, col int, val byte) bool {
	l := len(board)
	for i := 0; i < l; i++ {
		if board[row][i] == val {
			return false
		}
	}
	for i := 0; i < l; i++ {
		if board[i][col] == val {
			return false
		}
	}
	startRow := (row / 3) * 3
	startCol := (col / 3) * 3
	for i := startRow; i < startRow+3; i++ {
		for j := startCol; j < startCol+3; j++ {
			if board[i][j] == val {
				return false
			}
		}
	}
	return true
}

